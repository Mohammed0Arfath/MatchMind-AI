export function getActiveIncidents(): Incident[] {
  return incidents.filter((i) => i.status !== 'resolved' && i.status !== 'escalated');
}

export function getIncidentsByPriority(priority: Priority): Incident[] {
  return incidents.filter((i) => i.priority === priority);
}

export function getIncidentsByType(type: IncidentType): Incident[] {
  return incidents.filter((i) => i.type === type);
}

export function getRecentIncidents(count: number = 10): Incident[] {
  return [...incidents].sort((a, b) => b.reportedAt - a.reportedAt).slice(0, count);
}
